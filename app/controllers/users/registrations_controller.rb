  def create
    name = params[:user][:profile_pages][:name]
    last_name = params[:user][:profile_pages][:last_name]
    location = params[:user][:profile_pages][:location]
    @profile_page = ProfilePage.new(name: name, last_name: last_name, location: location)
    username = params[:user][:username]
    password = params[:user][:password]
    email = params[:user][:email]
    @user = User.new(email: email, password: password, username: username)
    @user.profile_page = @profile_page
    respond_to do |format|
      if @user.save
        format.html { redirect_to new_user_session_url, notice: 'User was successfully created.' }
        format.json { render :show, status: :created, location: @user }
      else
        format.html { render :new }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

